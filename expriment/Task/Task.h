#pragma once
/*****************************************************************************/
/* タスク(周期的に呼びたい関数)                                              */
/*****************************************************************************/

/*---------------------------------------------------------------------------*/
/* 型定義                                                                    */
/*---------------------------------------------------------------------------*/

typedef void (*TaskFunction)(void);

/* タスク */
typedef struct {
	bool(*execute)(void);
} Task;

/* タスク列 */
typedef struct {
	const Task* tasks;
	int length;
} TaskList;

/*---------------------------------------------------------------------------*/
/* 公開関数                                                                  */
/*---------------------------------------------------------------------------*/

/*****************************************************************************/
/* タスク列の実行 */
/* 与えられたタスクリストのタスクを実行する。中断できるならtrueを返す。 */
/* 中断できない = 時間がかかる処理の途中で、以降も周期処理が必要 */
/*****************************************************************************/
bool TaskListExecute(const TaskList* pList);
